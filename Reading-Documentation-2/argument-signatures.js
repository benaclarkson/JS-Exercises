/**
 * Task: How many arguments does the Array.prototype.join() method expect? What happens if you call it with less or more arguments?

 * Solution: The `Array.prototype.join()` method expects 0 or 1 arguments. If you pass none in, the elements of the array will be joined with a comma between each within the string. If more than 1 argument is passed in, the others are ignored.
**/